<h1 class="title">Реклама на бигбордах</h1>
<div class="row-fluid" style="margin-top: 20px; text-align: left;" >
	<div class="span12">
	
<p>Билборды (Николаев). Данный термин используется для описания вида наружной рекламы, устанавливаемой вдоль трасс, улиц. Щит представляет собой раму, обитую листами оцинкованной стали или фанеры, покрытую атмосфероустойчивыми составами, закреплённую на опоре. Появился этот термин в США, когда ряд компаний начали вывешивать свои рекламные плакаты «биллы» (отсюда и название Билборд) на деревянных конструкциях.</p>
<p>Появление рекламных щитов родилось из необходимости донести информацию необразованному населению. Бигборды (Николаев) имеют очень длинную историю развития, которая началась ещё с Древнего Египта. Поначалу это были объявления о награде за поимку беглых рабов. Далее в истории известны предшественники рекламных щитов - плакаты. Их появление стало возможным благодаря изобретению литографии (вида печати). Плакаты использовались в конце XIX века для рекламы театральных представлений. Но первым крупномасштабным использованием плакатов стало использование их для рекламы цирковых представлений.</p>
<p>К 20-м годам XX века всё больше людей в США стали приобретать автомобили, выезжать на них за город. Это стало причиной того, что реклама на билбордах (Николаев) стала увеличится на дороге в количестве, размерах (так как увеличилась скорость передвижения).</p>
<p>В странах бывшего СССР аренда билбордов (Николаев) стала развиваться по мере развития рыночных отношений и необходимости в условиях конкуренции рекламировать свой товар.</p>
<p>В странах Содружества Независимых Государств получил распространение термин «бигборд» (bigboard), дословно «большая доска». Причиной этому является то, что в странах бывшего Советского Союза в 90-х годах на рекламном рынке была широко представлена компания Big Board. Внизу рекламные щиты в Николаеве имели подпись «Big Board», вследствие этого рекламные щиты и стали называть бигбордами.</p>
